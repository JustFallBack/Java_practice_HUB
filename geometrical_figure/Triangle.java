/*
 * Triangle.java
 * This class extends the Polygon class.
 * It represents a triangle in a 2D space.
 */


public class Triangle extends Polygon {
    
    
    // Constructor, initialize the number of edges to 3 and the length of each edge.
    public Triangle(double[] edges) {
        super(3, edges);
    }


    // Return the perimeter of the triangle.
    public double perimeter() {
        return super.perimeter();
    }
}
